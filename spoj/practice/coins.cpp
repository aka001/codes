ll mem[1000001];
ll final_answer(ll n)
{
	if(n<=MAX && mem[n]!=-1)
		return mem[n];
	if(n<=1)
		return n;
	ll ans=n,calc;
	calc=final_answer(n/2)+final_answer(n/3)+final_answer(n/4);
	ans=max(ans,calc);
	if(n<=MAX)
		mem[n]=ans;
	return ans;
}

