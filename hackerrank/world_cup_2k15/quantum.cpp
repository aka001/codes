vector<pair<int,double> > arr[MAX];
double ans=0, vit[MAX];
int visited[MAX];
void dfs(int ind, double val, int comp)
{
	if(visited[ind]==comp)
	{
		double calc = val/(vit[ind]*1.0);
		ans+=calc;
		//trace5(ind, val, vit[ind],calc,ans);
		return;
	}
	else if(visited[ind])
		return;
	vit[ind]=val;
	visited[ind]=comp;
	int i,sz;
	sz=arr[ind].size();
	rep(i,sz)
	{
		dfs(arr[ind][i].F, val*arr[ind][i].S, comp);
	}
}
